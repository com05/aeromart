		<h1 class="page-title">한국 항공우주산업 개요</h1>
	</header><!-- .entry-header -->
	<div class="entry-content">
		<hr />
<p class="one-half"><img class="size-full wp-image-138 aligncenter" alt="jet-fighter" src="wp-content/uploads/2014/02/jet-fighter.jpg" width="1200" height="512" /><br />
&nbsp;2013년, <b class="green">한국항공산업 총생산량은 글로벌시장의 0.7%에 해당하는 36억 달러</b>를 기록하였습니다. 한국항공산업은 다수의 국방 과제들에 대해 정부의 해외구매 등을 통한 군용 항공기 및 부품생산 등에 초점을 맞춰 왔습니다.<br/>
&nbsp;대표적으로 진행 중인 핵심 사업으로는 <b class="green">‘F-X’ 사업, KHP projects의 최초양산 및 T-50 계열의 후속양산 등 군 관련 사업분야</b>가 있습니다.<br />
* 하지만, <b class="green">보잉 및 에어버스와의 제휴를 통해</b> 민수분야에 대해 괄목할 만한 수출증가를 달성해 왔습니다.<br /><br />
<small>* 대한민국 투자 &#8211; 항공우주</small></p>
<p class="second-half">
또한 최신형 민항기의 특정부품들도 한국에서 제조되었을 만큼 한국 항공부품산업의 중요성은 날로 증가하고 있습니다.<br />
한국에 대한 <b class="green">해외 투자유치를 위한 조세특례법</b>과 같은 정부의 지원계획과 함께 한국항공시장에 대한 성장가능성과 확장가능성은 증가하고 있습니다. <br />
이러한 정부의 계획 및 지원을 통해 보잉은 BTSK(보잉의 한국훈련지원센터)를 설립-투자하여 <b class="green">한국과 중국, 타이완, 몽골에 파견</b>될 새로운 조종사들을 교육하고 훈련시키는 사업의 성과를 거두고 있습니다.<br />
BTSK는 현재 프로그램을 확장할 계획에 있습니다. 또한 <b class="green">선진기술을 적용하고 더 정교한  항공전문기술을 개발할 새로운 항공교육센터를 건립할 계획</b>에 있으며, 이 센터는 2014년에 완공될 예정입니다.<br />
<br />
<small>* 대한민국 투자 &#8211; 항공우주</small></p>
